const nEntero = 5

if(nEntero === 0){
  console.log(`El numero ${nEntero} es Nulo`)
}else if(nEntero >= 1){
  console.log(`El número ${nEntero} es Positivo`)
}else{
  console.log(`El número ${nEntero} es Negativo `)
}
