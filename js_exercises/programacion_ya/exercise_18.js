// Ingresar por teclado 3 numeros y mostrar por pantalla el promedio si es mayor a 7

let num1 = 10
let num2 = 7
let num3 = 5

promedio = (num1 + num2 + num3) / 3

if (promedio >= 7){
  console.log(`El promedio es ${promedio} estas PROMOCIONADO`)
}else{
  console.log(`El promedio es ${promedio} no estas PROMOCIONADO`)
}